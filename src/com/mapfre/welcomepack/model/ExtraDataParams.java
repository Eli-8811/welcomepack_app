package com.mapfre.welcomepack.model;

public class ExtraDataParams {

	private String productoComercial;
	private String idProductoComercial;
	private String docCondicionesGenerales;
	
	private String dataCNSF;
	private String dataRECAS;
	
	public String getProductoComercial() {
		return productoComercial;
	}
	public void setProductoComercial(String productoComercial) {
		this.productoComercial = productoComercial;
	}
	public String getIdProductoComercial() {
		return idProductoComercial;
	}
	public void setIdProductoComercial(String idProductoComercial) {
		this.idProductoComercial = idProductoComercial;
	}
	public String getDocCondicionesGenerales() {
		return docCondicionesGenerales;
	}
	public void setDocCondicionesGenerales(String docCondicionesGenerales) {
		this.docCondicionesGenerales = docCondicionesGenerales;
	}
	public String getDataCNSF() {
		return dataCNSF;
	}
	public void setDataCNSF(String dataCNSF) {
		this.dataCNSF = dataCNSF;
	}
	public String getDataRECAS() {
		return dataRECAS;
	}
	public void setDataRECAS(String dataRECAS) {
		this.dataRECAS = dataRECAS;
	}
	
}